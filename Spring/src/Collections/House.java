package Collections;

public class House {

	
	String app1;
	int roomNo;
	String bedroom;
	public String getApp1() {
		return app1;
	}
	public void setApp1(String app1) {
		this.app1 = app1;
	}
	public int getRoomNo() {
		return roomNo;
	}
	public void setRoomNo(int roomNo) {
		this.roomNo = roomNo;
	}
	public String getBedroom() {
		return bedroom;
	}
	public void setBedroom(String bedroom) {
		this.bedroom = bedroom;
	}
	
	
	
}
